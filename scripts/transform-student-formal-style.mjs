function transform(value) {
  if (greetings.has(value)) return value;
  const greetingMarker = '__GREETING__';
  let next = value.replaceAll('안녕하세요', greetingMarker);
  for (const [pattern, replacement] of replacements) next = next.replace(pattern, replacement);
  next = next
    .replace(/([가-힣]+)네요/g, '$1입니다')
    .replace(/([가-힣]+)지요/g, '$1습니다')
    .replace(/([가-힣]+)답니다/g, '$1습니다')
    .replace(/([가-힣]+)군요/g, '$1습니다')
    .replace(/([가-힣]+)요(?=[.!?]|$)/g, '$1습니다')
    .replaceAll(greetingMarker, '안녕하세요');
  return next;
}

for (const file of files) {
  const source = fs.readFileSync(file, 'utf8');
  const literalUpdated = source.replace(/(['"`])(?:\\.|(?!\1)[^\\])*\1/g, (literal) => {
    const quote = literal[0];
    const value = literal.slice(1, -1);
    return `${quote}${transform(value)}${quote}`;
  });
  const updated = /src[\\/]utils[\\/]|src[\\/]types\.ts$|src[\\/]views[\\/]TeacherView\.tsx$/.test(file)
    ? literalUpdated
    : transform(literalUpdated);
  if (updated !== source) fs.writeFileSync(file, updated);
}
